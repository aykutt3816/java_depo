package day05operators;

public class Decrement01 {

	public static void main(String[] args) {
		// int data tipinde bir variable olusturun
	// bunu 3 yöntemide kullanarak 1 azaltın
	
		int sayi1=12;
		
		sayi1=sayi1-1;// uzun
		System.out.println(sayi1);
		sayi1-=1;//kısa
		System.out.println(sayi1);
		sayi1--;//6   en kısa
		System.out.println(sayi1);
		
		
		
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	}

}
