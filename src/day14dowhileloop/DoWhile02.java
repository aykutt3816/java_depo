package day14dowhileloop;

public class DoWhile02 {

	public static void main(String[] args) {
		// doWhile ile while looplarýnýn farký
        //doWhile ENAZ BÝRKERE ÇALIÞIR
	int num1 =6;
	
	while(num1<7) {
		System.out.println(num1);
		num1++;
	}
	
	int num2=7;
	do {
		System.out.println(num2);
		num2++;
	}while(num2<7);
	
	
	
	
	
	
	
	
	
	
	}

}
